<template>
  <v-container fluid fill-height>
        <v-layout align-center justify-center>
          <v-flex xs12 sm8 md4>
            <v-card class="elevation-12">
              <v-toolbar dark color="primary">
                <v-toolbar-title>Вход</v-toolbar-title>
                <v-spacer></v-spacer>
              </v-toolbar>
              <v-card-text>
                <v-form>
                    <v-text-field outline label="Login or E-mail" append-icon="person" v-model="login"></v-text-field>
                     <v-text-field outline label="Password" @click:append="show1 = !show1" :append-icon="show1 ? 'visibility_off' : 'visibility'" :type="show1 ? 'text' : 'password'" required v-model="password" :rules="passwordRules"></v-text-field>
                    <v-switch v-model="remember_me" label="Запомнить меня"></v-switch>
                </v-form>
              </v-card-text>
              <v-card-actions>
                <v-spacer></v-spacer>
                <v-btn flat color="primary" to="/registration">Регистрация</v-btn>
                <v-btn :disabled="!valid" color="primary" @click="submit">Отправить</v-btn>
              </v-card-actions>
            </v-card>
          </v-flex>
        </v-layout>
      </v-container>    
</template>
<script>
import { mapActions } from "vuex";
export default {
  data: () => ({
    remember_me: false,
    login: "",
    password: "",
    show1: false,
    passwordRules: [v => !!v || "Password is required"]
  }),
  methods: {
    ...mapActions({ SetToken: "user/SetToken" }),

    submit() {
      const init = this;
      if (this.login.indexOf("@") > -1)
        axios
          .post("/api/auth/login", {
            email: init.login,
            password: init.password,
            remember_me: true
          })
          .then(function(resp) {
            init.SetToken(resp.data);
            init.$router.push("/app");
          });
      else
        axios
          .post("/api/auth/login", {
            name: init.login,
            password: init.password,
            remember_me: true
          })
          .then(function(resp) {
            init.SetToken(resp.data);
            init.$router.push("/app");
          });
    }
  },
  computed: {
    valid() {
      if (this.password !== "" && this.login !== "") return true;
      else return false;
    }
  },
  beforeCreate() {
    if (this.$store.state.user.token) {
      if (Object.keys(this.$store.state.user.token).length > 0)
        this.$router.push("/app");
    }
  }
};
</script>
